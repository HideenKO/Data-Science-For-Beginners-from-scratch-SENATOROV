# 5. **Что такое интерпретатор?**
#    - Программа, которая построчно выполняет (интерпретирует) код.
#
# 6. **Где можно выбрать интерпретатор?**
#    - В VS Code / Jupyter — там, где показывается Kernel (справа/вверху
